// Send printf to uart2
int _write(int fd, char* ptr, int len) {
  HAL_StatusTypeDef hstatus;

  if (fd == 1 || fd == 2) {
    hstatus = HAL_UART_Transmit(&huart2, (uint8_t *) ptr, len, HAL_MAX_DELAY);
    if (hstatus == HAL_OK)
      return len;
    else
      return -1;
  }
  return -1;
}

void process_character(char ch) {
    static uint32_t strlen = 0;
    switch (ch) {
    case '\0':
        printf("Received    : %s\n", receive_buffer);
        receive_buffer[0] = '\0';
        strlen = 0;
        break;
    default:
        if (strlen < BUFFER_SIZE) {
            strncat((char*) &receive_buffer, &ch, 1);
            ++strlen;
        }
    }
}

void HAL_UARTEx_RxEventCallback(UART_HandleTypeDef *huart, uint16_t offset){
    static uint16_t last_offset = 0;
    printf("Offset %d\n", offset);

    if (offset != last_offset) {
        if (offset < last_offset)
            last_offset = 0;

        while (last_offset < offset) {
            process_character((char) dma_buffer[last_offset]);
            ++last_offset;
        }
    }
}

void HAL_UART_ErrorCallback(UART_HandleTypeDef *huart){
	printf("UART Error!\n");
	Error_Handler();
}

